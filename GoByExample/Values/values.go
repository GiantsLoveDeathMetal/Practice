package main

import "fmt"

func main() {
	// Basically strings
	fmt.Println("go" + "lang")

	// Basically Ints
	fmt.Println("1+1 =", 1+1)

	// Basically Floats
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// Basically Booleans And comparison
	fmt.Println(true && false)

	// Basically Booleans Or comparison
	fmt.Println(true || false)

	// Basically Booleans not
	fmt.Println(!true)
}
